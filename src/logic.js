export const eventLibrary = Object.entries(eventConfig).map(([title, config]) => ({ title, ...config }));

export function getEmotionAdjustment(emotion) {
  if (emotion === '急需安排') return 12;
  if (emotion === '非常焦虑') return 10;
  if (emotion === '有点焦虑') return 5;
  return 0;
}

export function getMaterialAdjustment(materialStatus) {
  if (materialStatus === '有体检报告' || materialStatus === '有影像资料' || materialStatus === '有既往病历') return 6;
  if (materialStatus === '资料不完整') return 3;
  return 0;
}

export function getEventPlan(client) {
  const config = eventConfig[client.eventType] || eventConfig['体检报告异常'];
  const priorityScore = Math.min(100, config.urgencyBase + getEmotionAdjustment(client.emotion) + getMaterialAdjustment(client.materialStatus));
  const priorityLabel = priorityScore >= 88 ? '优先处理' : priorityScore >= 72 ? '建议尽快跟进' : '适合持续关怀';
  return { ...config, priorityScore, priorityLabel, internalSummary: `${client.name}｜${client.subject}｜${client.ageGroup}｜${client.region}｜${client.eventType}` };
}

export function generateScript(client, plan, tone) {
  const base = plan.scriptByTone?.[tone] || plan.scriptByTone?.balanced || '';
  const note = client.note?.trim();
  if (!note) return base;
  if (tone === 'professional') return `${base}\n\n我会先按您补充的情况整理重点：${note}`;
  if (tone === 'warm') return `${base}\n\n我看到您提到“${note}”，这个信息我会一起放进资料清单里，后面和医生沟通时也更不容易遗漏。`;
  return `${base}\n\n您刚刚补充的情况是：${note}。我会先把这部分和现有资料一起整理，方便后续判断下一步路径。`;
}
